package com.infoservices.lue.adapterdata;

public class CouponlistData {

	private String coupon_details;
	private String time;
	private String credits;
	private String serial;
	private String dealid;
	private String transid;
	public String getDealid() {
		return dealid;
	}
	public void setDealid(String dealid) {
		this.dealid = dealid;
	}
	public String getTransid() {
		return transid;
	}
	public void setTransid(String transid) {
		this.transid = transid;
	}
	public String getCoupon_details() {
		return coupon_details;
	}
	public void setCoupon_details(String coupon_details) {
		this.coupon_details = coupon_details;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getCredits() {
		return credits;
	}
	public void setCredits(String credits) {
		this.credits = credits;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
}
